#!/usr/bin/env node

/**
 * workspace-hub-link.js
 * Sibling repo ↔ hub linkage for IDE sessions opened on child checkouts.
 */

const fs = require('fs');
const path = require('path');
const { toPosixRelative } = require('./workspace-project-paths');

const HUB_LINK_REL = '.jumpstart/hub-link.json';
const SIBLING_WORKSPACE_DOC = '.jumpstart/SIBLING-WORKSPACE.md';
const CURSOR_RULE_REL = '.cursor/rules/jumpstart-sibling.mdc';
const CLAUDE_MARKER = '<!-- jumpstart-sibling-managed -->';

function hubLinkPath(repoRoot) {
  return path.join(repoRoot, HUB_LINK_REL);
}

function readHubLink(repoRoot = process.cwd()) {
  const filePath = hubLinkPath(repoRoot);
  if (!fs.existsSync(filePath)) {
    return null;
  }
  try {
    return JSON.parse(fs.readFileSync(filePath, 'utf8'));
  } catch {
    return null;
  }
}

function resolveHubRootFromLink(siblingRoot, link) {
  if (!link?.hub_root) {
    return null;
  }
  return path.normalize(path.resolve(siblingRoot, link.hub_root));
}

function writeHubLink(siblingRoot, { hubRoot, projectId, projectName }) {
  const payload = {
    version: '1.0.0',
    hub_root: toPosixRelative(siblingRoot, path.resolve(hubRoot)),
    project_id: projectId,
    project_name: projectName,
    linked_at: new Date().toISOString(),
  };
  const filePath = hubLinkPath(siblingRoot);
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, JSON.stringify(payload, null, 2) + '\n', 'utf8');
  return payload;
}

function buildSiblingWorkspaceDoc(context) {
  const lines = [
    '# JumpStart Sibling Workspace',
    '',
    '> Auto-generated by `jumpstart-mode workspace link-sibling`. Do not edit by hand — re-run link or `workspace context --write`.',
    '',
    '| Field | Value |',
    '|-------|-------|',
    `| Hub root | \`${context.hubRootRel || context.hubRoot}\` |`,
    `| Project ID | \`${context.project.project_id}\` |`,
    `| Project name | ${context.project.name} |`,
    `| Mode | ${context.mode} |`,
    `| Current phase | ${context.state?.current_phase ?? 'not started'} |`,
    `| Specs | \`${context.config?.specs_path || 'specs/'}\` |`,
    '',
    '## Agent rules',
    '',
    '1. Read and write specs only under **this repo** (`specs/`, `src/`, `tests/`).',
    '2. Run phase gates from the **hub** directory: `npx jumpstart-mode workspace set-active ' +
      `${context.project.project_id}\` (hub cwd).`,
    '3. Approve artifacts: `npx jumpstart-mode approve specs/<artifact>.md --approver <name>` from hub cwd.',
    '4. Status: `npx jumpstart-mode workspace status` from hub cwd.',
    '',
    'See hub guide: `.jumpstart/MULTI_WORKSPACE.md` in the hub repository.',
    '',
  ];
  return lines.join('\n');
}

function buildSiblingCursorRule(context) {
  return `---
description: JumpStart sibling repo — hub-linked project context
alwaysApply: true
---

# JumpStart sibling repository

This checkout is registered with a JumpStart **hub**. At every session start:

1. Read \`.jumpstart/hub-link.json\` and \`.jumpstart/SIBLING-WORKSPACE.md\`.
2. Scope all spec work to **this repo's** \`specs/\`, \`src/\`, \`tests/\`.
3. Run \`jumpstart-mode workspace *\` and \`jumpstart-mode approve\` from hub root: \`${context.hubRootRel || 'see hub-link.json'}\`.
4. Active project ID: \`${context.project.project_id}\` (${context.project.name}).

Do not assume root \`specs/\` on the hub unless \`proj-default\` is active there.
`;
}

function buildSiblingClaudeSnippet(context) {
  return `${CLAUDE_MARKER}

## JumpStart sibling hub link

This repo is linked to a JumpStart hub. **Read \`.jumpstart/SIBLING-WORKSPACE.md\` at session start.**

- Hub (run \`jumpstart-mode workspace\` / \`approve\` here): \`${context.hubRootRel}\`
- Project ID: \`${context.project.project_id}\`
- Specs for phase work: this repo's \`specs/\` only
`;
}

function mergeClaudeSiblingBlock(claudePath, snippet) {
  if (!fs.existsSync(claudePath)) {
    fs.writeFileSync(claudePath, snippet + '\n', 'utf8');
    return { action: 'created' };
  }

  const existing = fs.readFileSync(claudePath, 'utf8');
  if (existing.includes(CLAUDE_MARKER)) {
    const pattern = new RegExp(`${CLAUDE_MARKER}[\\s\\S]*?(?=\\n## |$)`);
    if (pattern.test(existing)) {
      fs.writeFileSync(claudePath, existing.replace(pattern, snippet.trim()), 'utf8');
      return { action: 'updated' };
    }
  }

  fs.writeFileSync(claudePath, `${existing.trim()}\n\n${snippet}\n`, 'utf8');
  return { action: 'appended' };
}

/**
 * Write IDE injection files into a sibling repo checkout.
 */
function writeSiblingIdeStubs(siblingRoot, context) {
  const written = [];

  const docPath = path.join(siblingRoot, SIBLING_WORKSPACE_DOC);
  fs.mkdirSync(path.dirname(docPath), { recursive: true });
  fs.writeFileSync(docPath, buildSiblingWorkspaceDoc(context), 'utf8');
  written.push(SIBLING_WORKSPACE_DOC);

  const cursorPath = path.join(siblingRoot, CURSOR_RULE_REL);
  fs.mkdirSync(path.dirname(cursorPath), { recursive: true });
  fs.writeFileSync(cursorPath, buildSiblingCursorRule(context), 'utf8');
  written.push(CURSOR_RULE_REL);

  const claudeResult = mergeClaudeSiblingBlock(
    path.join(siblingRoot, 'CLAUDE.md'),
    buildSiblingClaudeSnippet(context)
  );
  written.push(`CLAUDE.md (${claudeResult.action})`);

  return written;
}

const HUB_CURSOR_RULE_REL = '.cursor/rules/jumpstart-workspace.mdc';

function buildHubCursorRule() {
  return `---
description: JumpStart multi-project workspace (hub root)
alwaysApply: true
---

# JumpStart workspace hub

When \`.jumpstart/projects.json\` exists:

1. At session start, read \`.jumpstart/projects.json\` active project or run \`npx jumpstart-mode workspace active\`.
2. Scope specs to the active project's \`specs/\` path (see \`.jumpstart/MULTI_WORKSPACE.md\`).
3. For sibling repos (🔗), run \`jumpstart-mode workspace link-sibling\` from this hub; open the **linked checkout** for implementation work.
4. Phase gates: \`npx jumpstart-mode approve specs/<artifact>.md --approver <name>\` from **this hub cwd**.

Monorepo projects live under \`projects/{id}/\`. Sibling repos use \`../\` paths in the registry.
`;
}

function ensureHubCursorRule(hubRoot) {
  const rulePath = path.join(hubRoot, HUB_CURSOR_RULE_REL);
  if (fs.existsSync(rulePath)) {
    return null;
  }
  fs.mkdirSync(path.dirname(rulePath), { recursive: true });
  fs.writeFileSync(rulePath, buildHubCursorRule(), 'utf8');
  return HUB_CURSOR_RULE_REL;
}

module.exports = {
  HUB_LINK_REL,
  SIBLING_WORKSPACE_DOC,
  CURSOR_RULE_REL,
  HUB_CURSOR_RULE_REL,
  CLAUDE_MARKER,
  readHubLink,
  resolveHubRootFromLink,
  writeHubLink,
  buildSiblingWorkspaceDoc,
  writeSiblingIdeStubs,
  ensureHubCursorRule,
  buildHubCursorRule,
};
